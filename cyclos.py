"""
A cycle or loop, is a control 
structure in programming that allows a section 
of code to be repeated a certain number of times.
This repetition can be based on a condition,
a counter, or an iterable object.
"""

# for--> it has the defined number times
#example
fruits=['apple','banana','cherry','grapes','avocado']
for fruit in fruits:
    print(fruit)
#while--> it depends of the condition (TRUE)
i= 0
while i < 5:
    print(i)
    i += 1